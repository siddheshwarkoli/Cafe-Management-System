    public void validateFields() {
        String name = txtname.getText();
        String mobile = txtmobile.getText();
        String email = txtemail.getText();
        //String date = txtdate.getText();
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String date = sdf.format(txtdate.getDate()).toString();
        String totalbill = txttotalbill.getText();
        if (!name.equals("") && email.matches(emailPattern) && mobile.matches(mobilenoPattern) && mobile.length() == 10 && !date.equals("") && !totalbill.equals("")) {
            btnsave.setEnabled(true);
        } else {
            btnsave.setEnabled(false);
        }
    }
    
    
    public void BillNo()
    {
      try
      {
    
            
        
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("select MAX(id) from orderplaced");
        rs.next();
        
        rs.getString("MAX(id)");
        
        if(rs.getString("MAX(id)") == null)
        {
            txtbillnumber.setText("B-01");
        }
        else
        {
               long id = Long.parseLong(rs.getString("MAX(id)").substring(2,rs.getString("MAX(id)").length()));
             id++;
             txtbillnumber.setText("B-"+String.format("%02d",id));
        }
    }
        catch (SQLException ex) {
            Logger.getLogger(Ordernew.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
  

