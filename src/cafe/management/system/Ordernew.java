    private void txtpcodeKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpcodeKeyPressed
        // TODO add your handling code here:
        
      if(evt.getKeyCode() == KeyEvent.VK_ENTER)
      {
          String pcode=txtpcode.getText();
          
          try {
              Class.forName("com.mysql.cj.jdbc.Driver");
              con=DriverManager.getConnection("jdbc:mysql://localhost/cms","root","rootpassword");
              pst= con.prepareStatement("select * from newproduct where name = ?");
              
              pst.setString(1, pcode);
              rs = pst.executeQuery();
              
              if(rs.next()==false)
              {
                  JOptionPane.showMessageDialog(this,"Product Code Not Found");
                  
              }
              else
              {
                  String pname=rs.getString("category");
                  String price=rs.getString("prise");
                  
                  txtpname.setText(pname.trim());
                  txtprice.setText(price.trim());
              }
              
              
              
          } catch (ClassNotFoundException ex) {
              Logger.getLogger(Ordernew.class.getName()).log(Level.SEVERE, null, ex);
          } catch (SQLException ex) {
              Logger.getLogger(Ordernew.class.getName()).log(Level.SEVERE, null, ex);
          }
      }
        
        
        
        
        
        
    }//GEN-LAST:event_txtpcodeKeyPressed

